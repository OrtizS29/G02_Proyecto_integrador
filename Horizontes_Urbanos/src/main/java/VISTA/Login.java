    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        
        String usuario = txtUsuario.getText();
        String contrasena = txtContrasena.getText();
        Usuario usr = valiUsuario.validarUsuario(usuario,contrasena);
        
        if(usr != null) {
            String rol = usr.getRol();
            
            if(rol.equals("admin")){
                menuAdmin mAdmin = new menuAdmin();
                mAdmin.setVisible(true);
                this.dispose();
            }else if(rol.equals("asesor")){
                menuAsesor meAsesor = new menuAsesor();
                meAsesor.setVisible(true);
                this.dispose();
            }
        }
        else{
            JOptionPane optionPane = new JOptionPane("No se encontro el usuario");
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
            JDialog dialog = optionPane.createDialog("Error login");
            dialog.setAlwaysOnTop(true);
            dialog.setVisible(true);
        }
        
    }//GEN-LAST:event_btnEntrarActionPerformed

