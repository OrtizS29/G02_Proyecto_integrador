    private void cargarTabla() throws Exception {

        DefaultTableModel modeloTabla  = new DefaultTableModel(){
            
            @Override
            public boolean isCellEditable (int row,int column){
                return false;
            }
        };
        
        String titulos[] = {"Numero Proyecto","Nombre Proyecto"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        List<Proyecto> listaProyectos = gestiProyecto.obtenerProyectosAdmin();
        
        if(listaProyectos != null){
            for(Proyecto proyect: listaProyectos){
                Object[] objeto ={proyect.getId_proyecto(),proyect.getNombre_proyecto()};
                modeloTabla.addRow(objeto);
            }
        }
        
        tablaSeleccionarProyecto.setModel(modeloTabla);
        tablaSeleccionarProyecto.getTableHeader().setReorderingAllowed(false);
        
        TableColumnModel columnModel = tablaSeleccionarProyecto.getColumnModel();
        columnModel.getColumn(0).setMinWidth(0);
        columnModel.getColumn(0).setMaxWidth(0);
        columnModel.getColumn(0).setWidth(0);
        
        //Espaciado de filas
        tablaSeleccionarProyecto.setRowHeight(30); 
    }
