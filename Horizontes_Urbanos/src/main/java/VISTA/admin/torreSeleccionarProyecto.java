    private void btnSiguienteSeleccionarProyectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteSeleccionarProyectoActionPerformed

        btnSiguienteSeleccionarProyecto.setEnabled(false);

        if(tablaSeleccionarProyecto.getRowCount() > 0){
            if(tablaSeleccionarProyecto.getSelectedRow() != -1){
                
                Long id_proyecto = Long.parseLong(String.valueOf(tablaSeleccionarProyecto.getValueAt(tablaSeleccionarProyecto.getSelectedRow(), 0)));

                Proyecto proyectoSeleccionado = null;
                try {
                    proyectoSeleccionado = gestiProyecto.buscarPorId(id_proyecto);
                } catch (Exception ex) {
                    Logger.getLogger(torreSeleccionarProyecto.class.getName()).log(Level.SEVERE, null, ex);
                }
                administrarTorre adminTorre = new administrarTorre(proyectoSeleccionado);
                adminTorre.setVisible(true);
                adminTorre.setLocationRelativeTo(null);
                this.dispose();
                
            }
        }

        btnSiguienteSeleccionarProyecto.setEnabled(true);
    }//GEN-LAST:event_btnSiguienteSeleccionarProyectoActionPerformed

